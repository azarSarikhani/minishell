#include "minishell.h"

void	free_tokens(t_token **tokens)
{
	t_token	*tmp;

	while ((*tokens) != NULL)
	{
		tmp = *tokens;
		(*tokens) = (*tokens)->next;
		free(tmp->content);
		free(tmp);
	}
	free ((*tokens));
}
